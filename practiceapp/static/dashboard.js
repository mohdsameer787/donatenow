
function toggle(){
    let panal=document.querySelector(".panale")
    

    if(panal.classList=="new1"){
        panal.classList.toggle("logodiv2");
        panal.classList.toggle("panale");
    }
    
    
    panal.classList.toggle("new1");
    
}  
